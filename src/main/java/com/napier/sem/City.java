package com.napier.sem;

public class City {

    /**
     * city ID
     */
    public int id;

    /**
     * city name
     */
    public String name;

    /**
     * country's alpha-3 code that the city is from
     */
    public String countryCode;

    /**
     * city district
     */
    public String district;

    /**
     * city population
     */
    public int population;

}
